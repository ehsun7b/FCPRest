package com.ehsuhnbehravesh.persepolis.news;

public class PreviousNextMatchInfo {

	private MatchInfo preMatch;
	private MatchInfo nextMatch;

	public MatchInfo getPreMatch() {
		return preMatch;
	}

	public void setPreMatch(MatchInfo preMatch) {
		this.preMatch = preMatch;
	}

	public MatchInfo getNextMatch() {
		return nextMatch;
	}

	public void setNextMatch(MatchInfo nextMatch) {
		this.nextMatch = nextMatch;
	}
}
