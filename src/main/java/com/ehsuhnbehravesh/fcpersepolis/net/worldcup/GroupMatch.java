package com.ehsuhnbehravesh.fcpersepolis.net.worldcup;

/**
 *
 * @author ehsun7b
 */
public class GroupMatch {
  
  private String opponentEnglishName;
  private String opponentPersianName;
  private String localDate;
  private String localTime;
  private String location;

  public String getOpponentEnglishName() {
    return opponentEnglishName;
  }

  public void setOpponentEnglishName(String opponentEnglishName) {
    this.opponentEnglishName = opponentEnglishName;
  }

  public String getOpponentPersianName() {
    return opponentPersianName;
  }

  public void setOpponentPersianName(String opponentPersianName) {
    this.opponentPersianName = opponentPersianName;
  }

  public String getLocalDate() {
    return localDate;
  }

  public void setLocalDate(String localDate) {
    this.localDate = localDate;
  }

  public String getLocalTime() {
    return localTime;
  }

  public void setLocalTime(String localTime) {
    this.localTime = localTime;
  }

  public String getLocation() {
    return location;
  }

  public void setLocation(String location) {
    this.location = location;
  }
  
  
}
